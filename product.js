const urlParams = new URLSearchParams(window.location.search);
const productId = urlParams.get("id");

const product = products.find((p) => p.id === productId);

if (product) {
  const mainImg = document.getElementById("MainImg");
  const thumbnailContainer = document.getElementById("thumbnail-container");

  mainImg.src = product.images[0];
  mainImg.alt = product.name;

  thumbnailContainer.innerHTML = ""; // Clear old thumbnails

  product.images.forEach((image, index) => {
    const thumbDiv = document.createElement("div");
    thumbDiv.classList.add("small-img-col");

    const thumbImg = document.createElement("img");
    thumbImg.src = image;
    thumbImg.classList.add("small-img");
    thumbImg.alt = `${product.name} thumbnail ${index + 1}`;
    thumbImg.style.cursor = "pointer";

    thumbImg.addEventListener("click", () => {
      mainImg.src = image;
    });

    thumbDiv.appendChild(thumbImg);
    thumbnailContainer.appendChild(thumbDiv);
  });

  document.getElementById("productName").textContent = product.name;
  document.getElementById("productPrice").textContent = product.price;
  document.getElementById("productDescription").textContent =
    product.description;
} else {
  document.querySelector("#prodetails").innerHTML = "<p>Product not found.</p>";
}
